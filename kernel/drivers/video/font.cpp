const u8* Font::get_glyph(char c) {
    if (c < 32 || c > 126) c = '?';
    return &font_data[(c - 32) * 8];
}

void Font::draw_char(i32 x, i32 y, char c, u8 fg, u8 bg) {
    const u8* glyph = get_glyph(c);
    
    for (i32 row = 0; row < CHAR_HEIGHT; row++) {
        u8 bits = glyph[row];
        for (i32 col = 0; col < CHAR_WIDTH; col++) {
            if (bits & (0x80 >> col)) {
                Graphics::put_pixel(x + col, y + row, fg);
            } else if (bg != 0) {
                Graphics::put_pixel(x + col, y + row, bg);
            }
        }
    }
}

void Font::draw_string(i32 x, i32 y, const char* str, u8 fg, u8 bg) {
    while (*str) {
        draw_char(x, y, *str, fg, bg);
        x += CHAR_WIDTH;
        str++;
    }
}

i32 Font::string_width(const char* str) {
    i32 len = 0;
    while (*str++) len++;
    return len * CHAR_WIDTH;
}

